package logica;

public class Coordenada {
	private double x; 
	private double y; 
	
	public Coordenada (double X,double Y) {
		this.x= X; 
		this.y= Y; 
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}	

}
